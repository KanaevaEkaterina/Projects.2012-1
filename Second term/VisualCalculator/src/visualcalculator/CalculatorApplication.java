    /**
     * return result of calculation
     *
     * @param str1 first string for calculation @@param str2 second string for
     * calculation
     * @param sign symbol, that shows, what mathematical operation should be
     * done
     * @return string - result of calculation, or message about error
     */
    public String calculation(String str1, String str2, char sign) {
        if (sign == '+') {
            int a = Integer.parseInt(str1);
            int b = Integer.parseInt(str2);
            Integer res = (a + b);
            return res.toString();

        }
        if (sign == '-') {
            int a = Integer.parseInt(str1);
            int b = Integer.parseInt(str2);
            Integer res = (b - a);
            return res.toString();
        }
        if (sign == '*') {
            int a = Integer.parseInt(str1);
            int b = Integer.parseInt(str2);
            Integer res = (a * b);
            return res.toString();
        }
        if (sign == '/') {
            int a = Integer.parseInt(str1);
            int b = Integer.parseInt(str2);
            if (a != 0) {
                double res = ((double) b / (double) a);
                return String.valueOf(res);
            } else {
                return "Division by zero";
            }
        } else {
            return "Error";
        }
    }

